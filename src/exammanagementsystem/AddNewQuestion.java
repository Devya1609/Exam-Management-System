    private void save_btn1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_save_btn1ActionPerformed
        // TODO add your handling code here:
        String id = queid_txt.getText();
        String name = que_txt.getText();
        String op1 = op1_txt.getText();
        String op2 = op2_txt.getText();
        String op3 = op3_txt.getText();
        String op4 = op4_txt.getText();
        String answer = ans_txt.getText();
        
        
        try
        {  
            con = ConnectionProj.getConnection();
            pst = con.prepareStatement("insert into question values(?,?,?,?,?,?,?)"); //database query using preparaStatement
        
            pst.setString(1, id);
            pst.setString(2, name);
            pst.setString(3, op1);
            pst.setString(4, op2);
            pst.setString(5, op3);
            pst.setString(6, op4);
            pst.setString(7, answer);
            pst.executeUpdate();
            
            JFrame jf = new JFrame();
            jf.setAlwaysOnTop(true);
            JOptionPane.showMessageDialog(jf, "Successfully Updated");
            setVisible(false);
            
            new AddNewQuestion().setVisible(true);
            
            
        }
        catch(Exception e)
        {
            JFrame jf = new JFrame();
            jf.setAlwaysOnTop(true);
            JOptionPane.showMessageDialog(jf, e);
        }
    }//GEN-LAST:event_save_btn1ActionPerformed

