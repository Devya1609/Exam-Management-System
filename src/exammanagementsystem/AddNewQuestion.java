    public AddNewQuestion() {
        initComponents();
        try{
            con = ConnectionProj.getConnection();
            stmt = con.createStatement();
            rs = stmt.executeQuery("select count(id) from question");
            if(rs.first()) //it executed when have 1 value in database 
            {
                int id = rs.getInt(1);
                id  = id+1;     //id number increase by 1
                String st = String.valueOf(id);
                queid_txt.setText(st);
            }
            else
            {
                //not any single id value exit that case this condition executed 
                queid_txt.setText("1");
            }
        }    
        catch(Exception e)
        {
            JFrame jf = new JFrame();
            jf.setAlwaysOnTop(true);
            JOptionPane.showMessageDialog(jf, e);
            
        }
    }

