    private void del_btn1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_del_btn1ActionPerformed
        // TODO add your handling code here:
        String id = queid_txt.getText();

        
        try
        {
            con = ConnectionProj.getConnection();
            pst = con.prepareStatement("delete  from question  where id=?"); //database query using preparaStatement
            pst.setString(1, id);
            pst.executeUpdate();
            
            JFrame jf = new JFrame();
            jf.setAlwaysOnTop(true);
            JOptionPane.showMessageDialog(jf, "Successfully Deleted");
            setVisible(false);
            
            new DeleteQuestion().setVisible(true);
        }
        catch(Exception e)
        {
            JFrame jf = new JFrame();
            jf.setAlwaysOnTop(true);
            JOptionPane.showMessageDialog(jf, e);
        }
    }//GEN-LAST:event_del_btn1ActionPerformed

