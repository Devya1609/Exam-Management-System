    private void sear_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sear_btnActionPerformed
        // TODO add your handling code here:
        String id = queid_txt.getText();
        try
        {
            con = ConnectionProj.getConnection();
            stmt = con.createStatement();
            rs = stmt.executeQuery("select * from question where id='"+id+"'");
            if(rs.first())
            {
                
                que_txt.setText(rs.getString(2));
                op1_txt.setText(rs.getString(3));
                op2_txt.setText(rs.getString(4));
                op3_txt.setText(rs.getString(5));
                op4_txt.setText(rs.getString(6));
                ans_txt.setText(rs.getString(7));
                queid_txt.setEditable(false);
            }
            else
            {
                JFrame jf = new JFrame();
                jf.setAlwaysOnTop(true);
                JOptionPane.showMessageDialog(jf, "Question id does not Exist");
            }
        }
        
        catch(Exception e)
        {
            JFrame jf = new JFrame();
            jf.setAlwaysOnTop(true);
            JOptionPane.showMessageDialog(jf, e);
        }
    }//GEN-LAST:event_sear_btnActionPerformed

